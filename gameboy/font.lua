-- Punctuation and symbols (already in 0x20-0x2F, 0x3A-0x3F)

local font = {}

function font.text(str, fg, bg)
    fg = fg or {255, 255, 255}
    bg = bg or {0, 0, 0}
    local len = #str
    local w = len * 8
    local h = 8
    local img = {}
    for y = 1, h do
        img[y] = {}
        for x = 1, w do
            img[y][x] = {bg[1], bg[2], bg[3]}
        end
    end
    for ci = 1, len do
        local char = str:byte(ci)
        local cdata = chars[char]
        if cdata then
            local bx = (ci - 1) * 8
            for row = 1, 8 do
                local bits = cdata[row]
                for col = 1, 8 do
                    if bit32.band(bits, bit32.lshift(0x80, -(col - 1))) ~= 0 then
                        img[row][bx + col] = {fg[1], fg[2], fg[3]}
                    end
                end
            end
        end
    end
    return { w = w, h = h, data = img }
end

return font
